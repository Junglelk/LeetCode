/**
 * 这题不要求确切返回出栈元素，所以，实际上就是维护最小值而已...
 * 需要注意的是，虽然实际上并不需要真的出栈，但pop()操作还是会将原最小值踢出去，所以要额外维护。
 * 原本以为这题需要自己实现栈...这题解也是挺无奈的...
 */
class MinStack {
    Deque<Integer> xStack;
    Deque<Integer> minStack;

    public MinStack() {
        xStack = new LinkedList<Integer>();
        minStack = new LinkedList<Integer>();
        minStack.push(Integer.MAX_VALUE);
    }

    public void push(int x) {
        xStack.push(x);
        minStack.push(Math.min(minStack.peek(), x));
    }

    public void pop() {
        xStack.pop();
        minStack.pop();
    }

    public int top() {
        return xStack.peek();
    }

    public int getMin() {
        return minStack.peek();
    }
}

